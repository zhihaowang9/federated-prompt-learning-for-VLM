    test_data = TYImageNet_test_ds.data_detailed

    lab2cname = TYImageNet_train_ds.lab2cname
    classnames = TYImageNet_train_ds.classnames

    return (X_train, y_train, X_test, y_test, train_data, test_data, lab2cname, classnames)


